/* A static method is attached to a class rather than an instance of a class
 * An instance method is attached to an instance
 * 
 * An instance method would be accessed through an instance of a class, like this:
 * User me = new User(); <--Creating instance
 * me.instanceMethod(); <--Accessing instance method through an instance of a class
 * 
 * A static method is accessed directly through the class itself:
 * User.staticMethod(); <--Accessing the static method directly through the class
 */